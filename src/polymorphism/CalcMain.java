package polymorphism;

public class CalcMain {

	public static void main(String[] args) {

	}

	public int add(int a, int b) {
		int c = a + b;
		return c;

	}

	public int add(int a, int b, int c) {
		int d = a + b + c;
		return d;

	}

	public int add(int a, int b, int c, int d) {
		int e = a + b + c + d;
		return e;

	}

}
