package arithmeticcalculation;

public class Multiplication {

	public static void main(String[] args) {

	}

	// with out ARG and with Return type

	public int mul() {

		int a = 10, b = 15;
		int c = a * b;
		return c;

	}

	// with out ARG and with out Return type

	public void mul1() {

		int a1 = 10, b1 = 15;
		System.out.println(a1 * b1);

	}

	// with ARG and with Return type

	public int mul2(int a2, int b2) {

		int c2 = a2 * b2;
		return c2;

	}

	// with ARG and with with out Return type

	public void mul3(int a, int b) {

		a = 11;
		b = 12;

		int c = a * b;

	}

}
